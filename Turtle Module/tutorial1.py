# Basic of Turtle module

import turtle 

object = turtle.Turtle()
object.color("red")
object.pensize(10)
object.shape("turtle")

object.forward(100)
object.left(90)
object.penup()
object.forward(100)
object.pendown()
object.right(90)
object.forward(100)

object.speed(1)
object.end_fill()
